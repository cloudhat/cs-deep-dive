# 동일한 시스템에서 삽입 정렬과 병합 정렬의 구현을 비교한다고 가정합니다. 
# 크기가 n인 입력의 경우 삽입 정렬은 8n2 단계로 실행되는 반면 병합 정렬은 64n lg n 단계로 실행됩니다. 
# 삽입 정렬이 병합 정렬을 능가하는(삽입정렬이 병합정렬보다 빠를 때의) n 값은 무엇입니까?

import math

n = 2

while 8*(n**2) > 64*n*math.log2(n):
    n+=1

print(n-1)

