# 동일한 시스템에서 실행 시간이 100n2인 알고리즘이 실행 시간이 2n인 알고리즘보다 빠르게 실행되도록 하는 n의 가장 작은 값은 무엇입니까?

n = 1

while 100*(n**2) > 2**n:
    #처음으로 100*n**2 이 2**n 보다 작아지는 때의 n
    n+=1

print(n)